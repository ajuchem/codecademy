// Codecademy exercises
// Make a for, while and do loops

for (var i = 0; i < 3; i++) {
    console.log("You are awesome!");
}

var condition = true;
while (condition) {
    console.log("Really, really awesome!");
    condition = false;
}

var condition = false;
do {
    console.log("Awesomesauce!")
} while (condition);
